if (typeof TEXTS !== "undefined") {
  for (const lang of ["uz", "ru", "en"]) {
    if (TEXTS[lang]) Object.assign(TEXTS[lang], CL_TEXTS[lang]);
  }
}

// Tarjima yordamchisi — APP.t dan oladi, topilmasa o'zbekcha zaxira (DT bilan bir xil)
function CT(key) {
  if (typeof APP !== "undefined" && APP && APP.t && APP.t[key] !== undefined) return APP.t[key];
  if (CL_TEXTS.uz[key] !== undefined) return CL_TEXTS.uz[key];
  return "";
}
